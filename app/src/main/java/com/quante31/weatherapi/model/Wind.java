package com.quante31.weatherapi.model;

public class Wind {
	private int speed;
	private int deg;
	private int gust;
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getDeg() {
		return deg;
	}
	public void setDeg(int deg) {
		this.deg = deg;
	}
	public int getGust() {
		return gust;
	}
	public void setGust(int gust) {
		this.gust = gust;
	}
}
